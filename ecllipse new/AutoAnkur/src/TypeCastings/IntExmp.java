package TypeCastings;

public class IntExmp {

	public static void main(String[] args) 
	{
		int m = (int) 8.13;
		System.out.println(m);
		
		double n =(double) 8;
		System.out.println(n);
		
		float f = (float) 8.1;
		System.out.println(f);
		
		double d = 3.14f;
		System.out.println(d);
	}

}
